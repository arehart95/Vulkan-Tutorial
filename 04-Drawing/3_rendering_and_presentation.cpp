/* Everything is going to come together! We will write the drawFrame function that will be called
	from the main loop to put the triangle on the screen. Create the function and call it from
	main loop.
	
	Outline of a Frame:
	
	At a high level, rendering a frame in Vulkan consists of a common set of steps:
		Wait for the previous frame to finish.
		Acquire an image from the swap chain.
		Record a command buffer which draws the scene onto that image.
		Submit the recorded command buffer.
		Present the swap chain image.
	This is the core of the render loop.
	
	Synchronization:
	
	A core design philosophy in Vulkan is that synchronization of execution on the GPU is
	explicit. The order of operations is up to the programmer to define various sync primitives
	which tell the driver the order we want things to run in. This means that many Vulkan API
	calls which start executing work on the GPU are asynchronous, meaning that the functions will
	return before the operation has finished.
	
	There are a number of events that need to be ordered explicitly because they happen on the
	GPU, such as:
		Acquire an image from the swap chain.
		Execute commands that draw onto the acquired image.
		Present that image to the screen for presentation, returning it to the swapchain. 
		
	Each of these events is set in motion using a single function call, but are all executed
	asynchronously. Thus, we need to explore which primitives we can use to achieve the desired
	ordering.
	
	Semaphores:
	
	A semaphore is used to add order between queue operations. These refer to the workd we submit
	to a queue, either in a command buffer or from within a function. Semaphores are used both to
	order work inside the same queue and between different queues. 
	
	There are two kinds of semaphores in Vulkan: binary and timeline. Only binary semaphores will
	be used in this tutorial, so further mention of the term semaphore refers exclusively to
	binary semaphores.
	
	A semaphore is either unsignaled or signaled. It begins its life as unsignaled. The way we use
	a semaphore to order queue operations is by providing the same semaphore as a 'signal' 
	semaphore in one queue operation and as a 'wait' semaphore in another queue operation. 
	
	Let's say we have semaphore S and queue operations A and B that we want to execute in order. 
	What we tell Vulkan is that operation A will 'signal' semaphore S when it finishes executing,
	and operation B will 'wait' on semaphore S before it begins executing. When operation A 
	finishes, semaphore S will be signaled, while operation B won't start until S is signaled. 
	After operation B begins executing, semaphore S is automatically reset back to being unsignaled
	allowing it to be used again.
	
		VkCommandBuffer A, B = ... // record command buffers
		VkSemaphore S = ... // create a semaphore
		
		// enqueue A, signal S when done - starts executing immediately.
		vkQueueSubmit(work: A, signal: S, wait: None)
		
		// enqueue B, wait on S to start
		vkQueueSubmi(work: B, signal: None, wait: S)
	
	In this code snippet, both calls to vkQueueSubmit() return immediately; the waiting only happens
	on the GPU. The CPU continues running without blocking. To make the CPU wait, we need a 
	different primitive called Fences.
	
	Fences:
	
	A fence has a similar purpose in that it is used to synchronize execution, but it is for 
	ordering on the CPU, otherwise known as the host. If the host needs to know when the GPU has 
	finished something, we use a fence.
	
	Fences are either in a signaled or unsignaled state. Whenever we submit work to execute, we can
	attach a fence to that work. When the work is finished, the fence will be signaled. Then we can
	make the host wait for the fence to be signaled, guaranteeing that the work has finished before
	the host continues. 
	
	A concrete example is taking a screenshot. Let's say that the necessary work on the GPU has
	been completed. Now we need to transfer the image from the GPU over to the host and then save
	the memory to a file. We have command buffer A which executes the transfer and fence F. We
	submit command buffer A with fence F, then immediately tell the host to wait for F to signal. 
	This causes the host to block until command buffer A finishes execution. Thus we are safe to
	let the host save the file to disk, as the memory transfer has completed. 
	
		VkCommandBuffer A = ... // record command buffer with the transfer
		VkFence F = // create the fence
		
		// enqueue A, start work immediately, signal F when done
		vkQueueSubmit(work:A, fence: F)
		
		vkWaitForFence(F) // blocks execution until A has finished
		
		save_screenshot_to_disk() // can't run until the transfer has finished
	
	Unlike the semaphore example, this does block host execution. In general, it is preferable to
	not block the host unless necessary. We want to feed the GPU and the host with useful work to do.
	Thus we prefer semaphores, or other sync primitives to sync our work. 
	
	Fences must be reset manually to put them back into an unsingaled state. This is because the 
	fence is used to control the execution of the host, and so the host gets to decide when to 
	reset the fence. 
	
	
	Which To Choose?
	
	We have two sync primitives and two places to apply syncs: swap chain operations and
	waiting for the previous frame to finish. We want to use semaphores for swap chain operations
	because they happen on the GPU and we don't want to make the host wait around if we can help it.
	For waiting on the previous frame to finish, we want to use fences for the opposite reason, 
	because we need the host to wait. This is so we don't draw more than one frame at a time. 
	Because we re-record the command buffer every frame, we cannot record the next frame's work to
	the command buffer until the current frame has finished executing, as we don't want to overwrite
	the current contents of the command buffer while the GPU is using it.
	
	We need one semaphore to signal that an image has been acquired from the swap chain and is
	ready for rendering, another one to signal that the rendering has finished and presentation can
	happen, and a fence to make sure only one frame is rendering at a time. Create 3 class members to
	store these objects. */


