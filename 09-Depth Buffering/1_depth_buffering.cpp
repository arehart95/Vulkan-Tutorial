/*	If the program is run now, the fragments of the lower square will be drawn over the fragments
	of the upper square, because it comes later in the vertex array. However there are two ways
	to solve this:
			Sort all of the draw calls by depth from back to front
			Use depth testing with a depth buffer
	The first approach is commonly used for transparent objects, because order-independent 
	transparency is a difficult challenge to solve. The problem of ordering fragments by depth
	is much more commonly solved using a depth buffer. A depth buffer is an additional attachment
	that stores the depth for every position, just like the color attachment stores the color of
	every position. Every time the rasterizer produces a fragment, the depth test will check if 
	the new fragment is closer than the previous one. If it isn't, then the new fragment is 
	discarded. A fragment that passes the depth test writes its own depth to the depth buffer. It
	is possible to manipulate this value from the fragment shader just like you can manipulate the
	color output. 
	
	The perspective projection matrix generated by GLM wil use the OpenGL depth range of -1.0 to
	1.0 by default. We need to configure it to use the Vulkan range of 0.0 to 1.0 using the
	GLM_FORCE_DEPTH_ZERO_TO_ONE defintion.
	
	A depth attachment is based on an image, just like the color attachment. The difference is 
	that the swap chain will not automatically create depth images for us. We only need a single 
	depth image because only one draw operation is running at once. The depth image will require
	the trifecta of resources: image, memory, and image view. Add the class members to hold the 
	handles for each. Create a new function createDepthResources and call it after 
	createCommandPool. */
